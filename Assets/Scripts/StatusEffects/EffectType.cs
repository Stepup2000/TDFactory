/// <summary>
/// Represents the different types of status effects that can be applied to enemies.
/// </summary>
public enum EffectType
{
    /// <summary>
    /// A fire-based effect that deals moderate damage in a short time.
    /// </summary>
    Fire,

    /// <summary>
    /// A poison-based effect that deals low damage over a longer period of time.
    /// </summary>
    Poison,
}
